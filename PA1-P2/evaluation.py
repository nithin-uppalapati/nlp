	def queryPrecision(self, query_doc_IDs_ordered, query_id, true_doc_IDs, k):
		"""
		Computation of precision of the Information Retrieval System
		at a given value of k for a single query

		Parameters
		----------
		arg1 : list
			A list of integers denoting the IDs of documents in
			their predicted order of relevance to a query
		arg2 : int
			The ID of the query in question
		arg3 : list
			The list of IDs of documents relevant to the query (ground truth)
		arg4 : int
			The k value

		Returns
		-------
		float
			The precision value as a number between 0 and 1
		"""

		precision = -1
		# print(query_doc_IDs_ordered)
		#Fill in code here
		cnt=0
		for i in range(k):
			if query_doc_IDs_ordered[i] in true_doc_IDs:
				cnt+=1

		precision = cnt/k			# intersec / retrieved
		# print(cnt)
		# print(k)

		return precision


	def meanPrecision(self, doc_IDs_ordered, query_ids, qrels, k):
		"""
		Computation of precision of the Information Retrieval System
		at a given value of k, averaged over all the queries

		Parameters
		----------
		arg1 : list
			A list of lists of integers where the ith sub-list is a list of IDs
			of documents in their predicted order of relevance to the ith query
		arg2 : list
			A list of IDs of the queries for which the documents are ordered
		arg3 : list
			A list of dictionaries containing document-relevance
			judgements - Refer cran_qrels.json for the structure of each
			dictionary
		arg4 : int
			The k value

		Returns
		-------
		float
			The mean precision value as a number between 0 and 1
		"""

		meanPrecision = -1

		#Fill in code here
		
		q_rel_docs={} ## {qID : List of Rel Docs}
		for i in query_ids:
			q_rel_docs.update({i:[]})

		sum_pres=0
		for i in qrels:
			if int(i["query_num"]) in query_ids:
				# temp = i["query_num"] - 1
				q_rel_docs[ int(i["query_num"]) ].append( int(i["id"]) )
		
		cnt=0
		for i in query_ids:
			sum_pres = sum_pres + self.queryPrecision(doc_IDs_ordered[cnt], i-1, q_rel_docs[i], k)
			cnt+=1

		meanPrecision = sum_pres/len(query_ids)

		return meanPrecision

	
